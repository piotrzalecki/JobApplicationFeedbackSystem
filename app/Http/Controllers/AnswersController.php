    {



      $feedback = Feedback::where('token',trim($id))->firstOrFail();

      $questionnaire = Questionnaire::where('id', $feedback->questionnaire_id)->firstOrFail();

      return view ('feedback.show', compact('feedback','questionnaire'));


    }

